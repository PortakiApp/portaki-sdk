import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { join } from 'node:path'

import type { PortakiFullModule } from '@portaki/sdk/author'

import { gatewayManifestSlice } from '../codegen/manifest-backend.js'
import { generateSchemaMigrations } from '../codegen/generate-schema-sql.js'
import { toMigrationBundle } from '../codegen/migration-bundle.js'
import { recordOperationsBundle } from '../codegen/record-operations.js'
import { mergeHybridManifest } from '../manifest/merge-hybrid-manifest.js'

export type BuildResult = {
  readonly outDir: string
  readonly module: PortakiFullModule
  readonly manifestPath: string | null
  readonly manifestUpdated: boolean
}

export type BuildOptions = {
  readonly outDir: string
  readonly manifestPath: string | null
}

export async function runBuild(module: PortakiFullModule, options: BuildOptions): Promise<BuildResult> {
  const outDir = options.outDir
  await mkdir(outDir, { recursive: true })
  await mkdir(join(outDir, 'backend'), { recursive: true })

  const manifestSlice = gatewayManifestSlice(module)
  await writeFile(join(outDir, 'manifest.backend.json'), JSON.stringify(manifestSlice, null, 2) + '\n')

  if (module.backend) {
    const revisions = generateSchemaMigrations(
      module.id,
      module.backend.schemaVersion,
      module.backend.schema,
    )
    const bundle = toMigrationBundle(module.id, module.backend.schemaVersion, revisions)
    await writeFile(join(outDir, 'migrations.bundle.json'), JSON.stringify(bundle, null, 2) + '\n')

    const operations = await recordOperationsBundle(module)
    await writeFile(join(outDir, 'operations.bundle.json'), JSON.stringify(operations, null, 2) + '\n')

    await writeFile(
      join(outDir, 'backend', 'artifact.json'),
      JSON.stringify(
        {
          moduleId: module.id,
          moduleVersion: module.version,
          runtime: 'wasm',
          wasmCompiler: 'assemblyscript',
          wasmStatus: 'shim',
          note: 'Uses platform gateway shim + operations.bundle.json until per-module AS wasm is emitted.',
          operations: {
            queries: Object.keys(module.backend.queries),
            commands: Object.keys(module.backend.commands),
          },
        },
        null,
        2,
      ) + '\n',
    )
  }

  let manifestPath: string | null = null
  let manifestUpdated = false
  if (options.manifestPath) {
    manifestPath = options.manifestPath
    const raw = await readFile(manifestPath, 'utf8')
    const existing = JSON.parse(raw) as Record<string, unknown>
    const { merged, changed } = mergeHybridManifest(existing, module)
    if (changed) {
      await writeFile(manifestPath, JSON.stringify(merged, null, 2) + '\n')
      manifestUpdated = true
    }
  }

  await writeFile(
    join(outDir, 'README.txt'),
    [
      'Generated by @portaki/module-cli — do not edit by hand.',
      'Commit src/portaki.module.ts + portaki.module.json (catalogue). Add .portaki/ to .gitignore.',
      '',
      'migrations.bundle.json → modules DB at deploy (private network).',
      'portaki.module.json → hybrid merge (queries/commands/database/scopes refreshed).',
    ].join('\n') + '\n',
  )

  return { outDir, module, manifestPath, manifestUpdated }
}
